    private void txt_jumlahtiketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_jumlahtiketActionPerformed
          int jumlahTiket = Integer.parseInt(txt_jumlahtiket.getText());

    // Get the age from txt_umur text field
    int umur = Integer.parseInt(txt_umur.getText());

    // Calculate the ticket price based on age
    int hargaTiket;
    if (umur < 17) {
        JOptionPane.showMessageDialog(this, "Usia harus di atas 17 tahun");
        return;
    } else if (umur >= 17 && umur <= 25) {
        hargaTiket = 50000;
    } else if (umur >= 26 && umur <= 35) {
        hargaTiket = 100000;
    } else {
        hargaTiket = 150000;
    }

    // Calculate the total price
    int total = jumlahTiket * hargaTiket;

    // Display the price and total in their respective text fields

    txt_total.setText("Rp" + Integer.toString(total));
    }//GEN-LAST:event_txt_jumlahtiketActionPerformed

