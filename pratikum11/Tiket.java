    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
       String nama = txt_nama.getText();
    int umur = Integer.parseInt(txt_umur.getText());
    int jumlahTiket = Integer.parseInt(txt_jumlahtiket.getText());

    String totalText = txt_total.getText();
    int total = Integer.parseInt(totalText.replaceAll("\\D+", ""));

    // Validate the age
    if (umur < 17) {
        JOptionPane.showMessageDialog(this, "Usia harus di atas 17 tahun");
        return;
    }

    // Increment the ticket number
    int currentTicketNumber = ticketNumber++;

    // Display the input results in a dialog message
    String message = "Data berhasil disimpan.\n\n"
            + "Nomor Tiket      : " + currentTicketNumber + "\n"
            + "Nama             : " + nama + "\n"
            + "Umur             : " + umur + "\n"
            + "Jumlah Tiket     : " + jumlahTiket + "\n"
            + "Total Harga      : Rp " + total;
    JOptionPane.showMessageDialog(this, message);

    // Clear the form fields
    txt_nama.setText("");
    txt_umur.setText("");
    txt_jumlahtiket.setText("");
    txt_total.setText("");

           }//GEN-LAST:event_btn_simpanActionPerformed

